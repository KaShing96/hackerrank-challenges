def missingNumbers(arr, brr):
    """
    Your code goes here.
    """
    # Store numbers
    # The key is the number, the value is an array of 2, with the first being the occurrneces in arr and the second being the occurrences in brr
    count = {}

    # Loop through arr and brr
    for ax, array in enumerate([arr, brr]):

        # Loop through each array
        for i in array: 

            # Ensure i exists
            if i not in count.keys(): 

                count[i] = [0, 0]

            count[i][ax] += 1
            
    # We obtain the answers
    answers = []

    for k, v in count.items(): 

        if v[0] != v[1]:
            
            answers.append(k)

    # Sort the answers
    answers.sort()

    return answers


